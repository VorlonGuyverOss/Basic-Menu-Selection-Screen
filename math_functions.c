#include <stdio.h>
#include <stdlib.h>
#include <math.h>

#include "math_functions.h"

int add ( int first, int last )
{
	return ( first + last );
}

int subtract ( int first, int last )
{
	return ( first - last );
}

int multiply ( int first, int last )
{
	return ( first * last );
}

float divide ( int first, int last )
{
	return ( ( float ) first / ( float ) last ) ;

}
